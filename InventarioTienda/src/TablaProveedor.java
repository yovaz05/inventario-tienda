    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
      AoM=1;
        //lblTitulo2.setText("Modificar Datos de Usuario");
        try{
            int codigo=Integer.parseInt(dt.getValueAt(tablaProveedores.getSelectedRow(), 0).toString());//Obtengo la codigo de la fila seleecionada
            String sql="SELECT * FROM proveedores WHERE codProveedor='"+codigo+"'";
            
            conn=Conexion.obtenerConexion();
            try {
                stmt=conn.createStatement();
                rs=stmt.executeQuery(sql);
                while(rs.next()){
                    tfNom.setText(rs.getString(2));
                    tfDir.setText(rs.getString(3));
                    
                }
                ////////////////MOSTRAR TELEFONOS SEGUN EL NUMERO DE TELEFONOS GUADADOS
                String sql2="SELECT * FROM tel_proveedor WHERE codProveedor='"+codigo+"'";
                rs=stmt.executeQuery(sql2);
                int x;//sirve para saber cuantos telefonos hay por cada usuario en la BD
                while(rs.next()){
                      x=rs.getRow();
                      if(x==1){
                          tfTel.setText(rs.getString(2));
                          l1.setVisible(false);
                          tfTel1.setVisible(false);
                          l2.setVisible(false);
                          tfTel2.setVisible(false);
                          l3.setVisible(false);
                          tfTel3.setVisible(false);
                          l4.setVisible(false);
                          tfTel4.setVisible(false);
                          l5.setVisible(false);
                          tfTel5.setVisible(false);
                          contClic=1;
                      }
                      if(x==2){
                          tfTel1.setText(rs.getString(2));
                          l1.setVisible(true);
                          tfTel1.setVisible(true);
                          contClic=2;
                      }
                      if(x==3){
                          tfTel2.setText(rs.getString(2));
                          l2.setVisible(true);
                          tfTel2.setVisible(true);
                          contClic=3;
                      }
                      if(x==4){
                          tfTel3.setText(rs.getString(2));
                          l3.setVisible(true);
                          tfTel3.setVisible(true);
                          contClic=4;
                      }
                      if(x==5){
                          tfTel4.setText(rs.getString(2));
                          l4.setVisible(true);
                          tfTel4.setVisible(true);
                          contClic=5;
                      }
                      if(x==6){
                          tfTel5.setText(rs.getString(2));
                          l5.setVisible(true);
                          tfTel5.setVisible(true);
                          contClic=6;
                      }
                }
                
                conn.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error al buscar: "+ex);
            }
        
        
        jdAccion.pack();
        jdAccion.setLocationRelativeTo(null);
        jdAccion.setVisible(true);
        }catch(ArrayIndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(null, "Debe elegir un proveedor");
        }
        
    }//GEN-LAST:event_btnModificarActionPerformed

