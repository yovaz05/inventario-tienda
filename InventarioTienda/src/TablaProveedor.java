    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if(AoM==0){//AoM=0: Funcion Agregar
            String sql;
            String nom=tfNom.getText();
            String dir=tfDir.getText();
            String tel=tfTel.getText();

            Boolean tel1=tfTel1.isVisible()&&tfTel1.getText().equals("    -    ");
            Boolean tel2=tfTel2.isVisible()&&tfTel2.getText().equals("    -    ");
            Boolean tel3=tfTel3.isVisible()&&tfTel3.getText().equals("    -    ");
            Boolean tel4=tfTel4.isVisible()&&tfTel4.getText().equals("    -    ");
            Boolean tel5=tfTel5.isVisible()&&tfTel5.getText().equals("    -    ");

            if(nom.equals("")||tel.equals("")||dir.equals("")||tel.equals("    -    ") ||tel1||tel2||tel3||tel4||tel5){
                JOptionPane.showMessageDialog(null, "No deje campos vacíos", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
            }else{

                
                sql="INSERT INTO proveedores(nombre,direccion,activo) VALUES('"+nom+"','"+dir+"','1')";
                f.insertar(sql);
                /////Obtengo el codigo del proveedor ingresado para guardar su telefono
                String codigo="SELECT * FROM proveedores";
                ResultSet rs = f.buscar(codigo);
                int cod = 0;
                try {
                    rs.last();
                    cod=rs.getInt(1);
                    //JOptionPane.showMessageDialog(null, cod);
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }
                f.cerrarConexion();
                String sql2="INSERT INTO tel_proveedor VALUES('"+cod+"','"+tel+"')";
                f.insertar(sql2);
                
                insertarTelExtra(cod);
              
                mostrarTabla("SELECT * FROM proveedores,tel_proveedor where proveedores.codProveedor=tel_proveedor.codProveedor and activo=1");
                jdAccion.dispose();
            }
             
        }else{//AoM=1: Funcion Modificar
            String nombre, primer, segundo, dir;
            nombre = tfNom.getText();
            dir = tfDir.getText();
            int codigo = Integer.parseInt(dt.getValueAt(tablaProveedores.getSelectedRow(), 0).toString());//Obtengo la codigo de la fila seleecionada
            if (nombre.equals("") || dir.equals("")) {
                JOptionPane.showMessageDialog(null, "No deje campos vacíos", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
            } else {
                String sql = "UPDATE proveedores SET nombre='" + nombre + "',direccion='" + dir + "' WHERE codProveedor='" + codigo + "'";
                f.insertar(sql);
                
                //Borro todos los numeros del proveedor para volverlos agregar a la tabla tel_proveedor
                sql="DELETE FROM tel_proveedor WHERE codProveedor='"+codigo+"'";
                f.insertar(sql);//elimina los datos de telefono del usuario correspondiente
                //Ingreso el telefono obligatorio
                String sql2="INSERT INTO tel_proveedor VALUES('"+codigo+"','"+tfTel.getText()+"')";
                f.insertar(sql2);
                //reingreso los telefonos extras
                insertarTelExtra(codigo);
            }
            
            mostrarTabla("SELECT * FROM proveedores,tel_proveedor where proveedores.codProveedor=tel_proveedor.codProveedor and activo=1");
            jdAccion.dispose();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

