     public void mostrarTabla(String sql){
        conn=Conexion.obtenerConexion();
        dt.setRowCount(0);
        try {
            stmt=conn.createStatement();
            rs=stmt.executeQuery(sql);
            while(rs.next()){
                dt.addRow(new Object[]{rs.getInt(1),rs.getString(2),rs.getString(3),rs.getString(6)});
            }
            conn.close();
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null, "Error al mostrar tabla "+ex,"ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }
     
    
