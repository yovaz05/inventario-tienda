    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        AoM=1;
        lblTitulo.setText("Modificar Datos de Cliente");
        //Recupero los datos de la BD para llenar los textfield
        try{
            int codigo=Integer.parseInt(dt.getValueAt(tablaClientes.getSelectedRow(), 0).toString());//Obtengo la codigo de la fila seleecionada
            String sql="SELECT * FROM clientes WHERE codCliente='"+codigo+"'";

            conn=Conexion.obtenerConexion();
            try {
                stmt=conn.createStatement();
                rs=stmt.executeQuery(sql);
                while(rs.next()){
                    tfNombre.setText(rs.getString(2));
                    tfPApellido.setText(rs.getString(3));
                    tfSApellido.setText(rs.getString(4));
                    tfDireccion.setText(rs.getString(5));
                }
                conn.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error al buscar: "+ex);
            }
            
            jdAccion.pack();
            jdAccion.setLocationRelativeTo(this);
            jdAccion.setVisible(true);
            
            
        }catch(ArrayIndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(null, "Debe elegir un cliente");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

