    private void btnFondoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFondoActionPerformed
        // TODO add your handling code here:
        
        jfc.setFileSelectionMode(JFileChooser.FILES_ONLY);
        FileNameExtensionFilter filtroImagen=new FileNameExtensionFilter("JPG","jpg");
        jfc.setFileFilter(filtroImagen);
        jfc.showOpenDialog(tsMenu);
        File f=jfc.getSelectedFile();
        String fil=f.getAbsolutePath().replace("\\", "\\\\");
        
        if (jfc.accept(f)) {
            panelFondo.setIcon(new javax.swing.ImageIcon(fil));
            javax.swing.GroupLayout panelFondoLayout = new javax.swing.GroupLayout(panelFondo);
        }
        try{
            FileInputStream fis=new FileInputStream(fil);
            FileOutputStream fos=new FileOutputStream(ultimoFondo);
            FileChannel inChannel=fis.getChannel();
            FileChannel outChannel=fos.getChannel();
            inChannel.transferTo(0, inChannel.size(), outChannel);
            fis.close();
            fos.close();
        }catch(IOException ioe){
            System.err.println(""+ioe);
        }
        ultimoFondo=fil;
    }//GEN-LAST:event_btnFondoActionPerformed

